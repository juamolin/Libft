#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;

	i = 0;
	while (i < n && (s1[i] != '\0' || s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int	main(void)
{
	char	cadena[] = "La vida es maravillosa";
	char	cadena2[] = "La vida es maravillosa";
	int	result;

	result = ft_strncmp(cadena, cadena2, 6);
	printf("%i", result);
	return (0);
}
